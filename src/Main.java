public class Main {
    public static void main(String[] args) {
        //For: Code x mal ausführen --> keine Abbruchbedingung notwendig
        //int i = 0; --> Erstellung Variable
        // i < 5; --> Anzahl der Durchläufe
        // i++ oder i += 3 --> Schrittgröße beim Zählen der Durchläufe (Zählweite/Schrittweite)
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }
    }
}